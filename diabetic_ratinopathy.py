    """Sharpens the image to highlight edges and finer details."""
    sharpen_kernel = np.array([[-1, -1, -1],
                               [-1,  9, -1],
                               [-1, -1, -1]])  # Sharpening kernel
    return cv2.filter2D(image, -1, sharpen_kernel)

